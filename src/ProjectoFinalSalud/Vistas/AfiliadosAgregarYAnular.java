    private void limpiar(){
        TextDNI.setText("");
        TextDomicilio.setText("");
        TextNYA.setText("");
        TextNumT.setText("");
        jLabel8.setText("");
        BRein.setEnabled(false);
        ButtonGuardar.setEnabled(true);
    }
    
    private boolean buscarDni(int dni) {
        ArrayList<Afiliado> afiliado = ad.listarAfiliados();
        boolean a = false;
        for (Afiliado alu : afiliado) {
            if (alu.getDni() == dni) {
                a = true;
                return a;
            }
        }
        return a;
    }
